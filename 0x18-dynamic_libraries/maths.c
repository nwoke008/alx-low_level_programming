#include <stdio.h>

/**
 *  add - fun to sum
 *  @a: first Integer
 *  @b: second integer
 *  Return: sun
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 *  sub - fun to subtract
 *  @a: first Integer
 *  @b: second integer
 *  Return: sun
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 *  mul - fun to find the product
 *  @a: first Integer
 *  @b: second integer
 *  Return: sun
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 *  mod - fun to find the quotient
 *  @a: first Integer
 *  @b: second integer
 *  Return: sun
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 *  div - fun to find the division
 *  @a: first Integer
 *  @b: second integer
 *  Return: sun
 */
int div(int a, int b)
{
	return (a / b);
}
